export class Dog {
  id: number;
  Name: string;
  Age: number;
  Gender: string;
  Type: string;
  Species: string;

  constructor(id:number,name:string,age:number,gender:string, type:string, species,string){
    this.id=id;
    this.Name = name;
    this.Age=age;
    this.Gender=gender;
    this.Type=type;
    this.Species=species;
  }
}
